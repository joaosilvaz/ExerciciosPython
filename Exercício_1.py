# Exercício 1) Considerando 2 números inteiros, faça a soma, subtração e multiplicação. Por fim, mostre os resultados.
num1 = int(input("Informe o primeiro número: "))
num2 = int(input("Informe o segundo número: "))

soma = num1 + num2
subtracao = num1 - num2
multiplicao = num1 * num2

print(f"A soma dos 2 números é {soma}")
print(f"A subtração dos 2 números é {subtracao}")
print(f"A multiplicação dos 2 números é {multiplicao}")
